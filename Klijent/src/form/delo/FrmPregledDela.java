    private int getSelectedRow() throws Exception {
        int selectedRow = tblPretraga.getSelectedRow();
        if (selectedRow == -1) {
            throw new Exception("Row is not selected!");
        }
        return selectedRow;
    }

    private void centerForm() {
        setLocationRelativeTo(null);
    }

    private void fixTable(List<Delo> lista) {
        TableModelDelo tmd = new TableModelDelo(lista);
        tblPretraga.setModel(tmd);
    }
