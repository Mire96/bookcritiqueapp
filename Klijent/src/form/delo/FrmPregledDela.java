    private void btnPretraziActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPretraziActionPerformed
        try {
            String pretragaNaslov = txtNaslov.getText().trim();
            String pretragaGodina = txtGodina.getText().trim();
            
            List<Delo> result = Controller.getInstance().getAllDela(pretragaNaslov, pretragaGodina);
            fixTable(result);
            if(result.size() > 0){
                btnPrikazi.setEnabled(true);
            }else{
                btnPrikazi.setEnabled(false);
                JOptionPane.showMessageDialog(null, "Nema nijednog dela!", "Pretraživanje dela", JOptionPane.INFORMATION_MESSAGE);
            }
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(FrmPregledDela.class.getName()).log(Level.SEVERE, null, ex);
        } catch (Exception ex) {
            Logger.getLogger(FrmPregledDela.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }//GEN-LAST:event_btnPretraziActionPerformed

