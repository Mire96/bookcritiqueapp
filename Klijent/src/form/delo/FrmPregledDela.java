    private void btnPrikaziActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPrikaziActionPerformed
        try {
            int selectedRow = getSelectedRow();
            TableModelDelo tmd = (TableModelDelo) tblPretraga.getModel();
            Delo delo = tmd.get(selectedRow);

            Session.getInstance().setCurrentUseCase(UseCase.UC_VIEW_DELO);
            Session.getInstance().getUseCaseParams().clear();
            Session.getInstance().getUseCaseParams().put("delo", delo);

            JOptionPane.showMessageDialog(null, "Uspešno su učitani podaci o delu", "Prikazivanje dela", JOptionPane.INFORMATION_MESSAGE);

            JDialog frmDelo = new FrmDelo(null, true, FrmMode.VIEW);
            frmDelo.setVisible(true);

            btnPretraziActionPerformed(evt);
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, "Sistem ne može da prikaže podatke o delu", "Prikazivanje dela", JOptionPane.ERROR_MESSAGE);
            System.err.println(ex.getMessage());
        }
    }//GEN-LAST:event_btnPrikaziActionPerformed

