    private void btnRegistrujActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrujActionPerformed
       
        
        try {
            
            String ime = txtIme.getText().trim();
            String prezime = txtPrezime.getText().trim();
            String username = txtUsername.getText().trim();
            String password = new String(txtPassword.getPassword());
            
            if(ime.isEmpty() || prezime.isEmpty() || username.isEmpty() || password.isEmpty())
                throw new Exception("Sva polja moraju biti popunjena");
            
            //Potencijalno kriptografiju ovde
            
            
            Controller.getInstance().register(username, password, ime, prezime);
            JOptionPane.showMessageDialog(this, "Uspesno ste se registrovali", "Uspesno registrovanje", JOptionPane.INFORMATION_MESSAGE);
            dispose();
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage(), "Greska!", JOptionPane.ERROR_MESSAGE);
        }
                
        
        
    }//GEN-LAST:event_btnRegistrujActionPerformed

    private void btnSacuvajActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSacuvajActionPerformed
        try {
            String ime = txtIme.getText().trim();
            String prezime = txtPrezime.getText().trim();
            String username = txtUsername.getText().trim();
            
            if(ime.isEmpty() || prezime.isEmpty() || username.isEmpty())
                throw new Exception("Sva polja moraju biti popunjena");
            
            Korisnik stari = (Korisnik) Session.getInstance().getUseCaseParams().get("korisnik");
            Korisnik novi = new Korisnik(stari.getKorisnikID(), ime, prezime, username, stari.getPassword(), stari.getAdminRole());
            
            Controller.getInstance().updateKorisnik(novi);
            JOptionPane.showMessageDialog(this, "Uspe≈°no ste saƒçuvali izmene!", "Izmena profila", JOptionPane.INFORMATION_MESSAGE);
            if(menu != null)
            menu.getLblKorisnik().setText("Dobrodosao/la " + novi.toString());
            dispose();
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Sistem ne mo≈æe da izmeni korisnika", "Greska!", JOptionPane.ERROR_MESSAGE);
            System.err.print(ex.getMessage());
            ex.printStackTrace();
        }
    }//GEN-LAST:event_btnSacuvajActionPerformed

