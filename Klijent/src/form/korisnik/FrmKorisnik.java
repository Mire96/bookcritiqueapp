    private void adjustForm(FrmMode mode) {
        setLocationRelativeTo(null);
        
        switch (mode){
            case NEW:
                
                btnSacuvaj.setVisible(false);
                btnCancel.setVisible(false);
                btnRegistruj.setVisible(true);
                txtPassword.setEnabled(true);
                setTitle("Novi korisnik");
                break;
            
            case EDIT:
                btnSacuvaj.setVisible(true);
                btnCancel.setVisible(true);
                btnRegistruj.setVisible(false);
                txtPassword.setEnabled(false);
                setTitle("Izmena korisnika");
                setKorisnik();
                break;
                
        }
    }

    private void setKorisnik() {
        Korisnik korisnik = (Korisnik) Session.getInstance().getUseCaseParams().get("korisnik");
        viewKorisnik(korisnik);
    }
    
    private void viewKorisnik(Korisnik korisnik) {
        txtIme.setText(korisnik.getIme());
        txtPrezime.setText(korisnik.getPrezime());
        txtUsername.setText(korisnik.getUsername());
        txtPassword.setText("");
        JOptionPane.showMessageDialog(this, "Uspe≈°no su uƒçitani podaci korisnika", "Izmena korisnika", JOptionPane.INFORMATION_MESSAGE);
    }
